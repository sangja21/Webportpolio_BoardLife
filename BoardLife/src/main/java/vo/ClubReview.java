package vo;

public class ClubReview {

	int club_review_num, club_num, club_review_rating;
	String user_id, club_review_title, club_review_content, club_review_date;
	
	public int getClub_review_num() {
		return club_review_num;
	}
	public void setClub_review_num(int club_review_num) {
		this.club_review_num = club_review_num;
	}
	public int getClub_num() {
		return club_num;
	}
	public void setClub_num(int club_num) {
		this.club_num = club_num;
	}
	public int getClub_review_rating() {
		return club_review_rating;
	}
	public void setClub_review_rating(int club_review_rating) {
		this.club_review_rating = club_review_rating;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getClub_review_title() {
		return club_review_title;
	}
	public void setClub_review_title(String club_review_title) {
		this.club_review_title = club_review_title;
	}
	public String getClub_review_content() {
		return club_review_content;
	}
	public void setClub_review_content(String club_review_content) {
		this.club_review_content = club_review_content;
	}
	public String getClub_review_date() {
		return club_review_date;
	}
	public void setClub_review_date(String club_review_date) {
		this.club_review_date = club_review_date;
	}
	
}
